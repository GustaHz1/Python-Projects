
aula = "Aula Python"
#Os simbolos [] indicam uma LISTA, a lista atribui mais de um valor para uma variavel
frutas = ["laranja, uva, limão"]
saldo = [1500, 100]
#O operador IN busca um valor especifico em uma variavel retornando se este valor esta presente na variavel
print("Python" in aula)
#O operador NOT IN é o contrario do IN, conferindo se um valor não esta na variavel
print("maca" not in frutas)
    
print(200 in saldo)