saldo = 200
print(saldo)

saldo += 100
print(saldo)
'''Atribuição de SOMA'''
saldo -= 100
print(saldo)
'''Atribuição de SUBTRAÇÂO'''
saldo *= 2
print(saldo)
'''Atribuição de MULTIPLICAÇÂO'''
saldo /= 2 
print(saldo)
'''Atribuição de DIVISÂO(FLOAT)'''
saldo //= 2
print(saldo)
'''Atribuição de DIVISÂO(INT)'''     
saldo **= 2
print(saldo)
'''Atribuição de POTÊNCIA'''
saldo %= 7
print(saldo)
